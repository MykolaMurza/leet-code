    public static int[] kWeakestRows(int[][] mat, int k) {
        int[] strength = new int[mat.length];
        for (int i = 0; i < mat.length; i++) {
            int lvlSum = 0;
            for (int j = 0; j < mat[i].length; j++) {
                lvlSum += mat[i][j];
            }
            strength[i] = lvlSum * 10000 + i;
        }

        for (int i = 0; i < strength.length - 1; i++) {
            for (int j = i + 1; j < strength.length; j++) {
                if (strength[i] > (strength[j])) {
                    int temp = strength[i];
                    strength[i] = strength[j];
                    strength[j] = temp;
                }
            }
        }

        int[] res = new int[k];
        for (int i = 0; i < res.length; i++) {
            String str = String.valueOf(strength[i]);
            int lastIndexOf = str.lastIndexOf("00");
            res[i] = Integer.parseInt(str.substring(lastIndexOf + 1));
        }

        return res;
    }
